c = float(input('Digite a temperatura em °C: '))
f = (9 * c / 5) + 32
print(' A temperatura em °F é: {:.1f}'.format(f))

'''A escala Fahrenheit foi criada em 1724 pelo físico e engenheiro Daniel Gabriel Fahrenheit. A escala é usada para medir a temperatura e a sua unidade é o grau Fahrenheit (°F). 
Origem da escala
Fahrenheit inventou o termômetro de mercúrio, que é usado até hoje. 
Ele usou a temperatura de uma mistura de gelo e cloreto de amônia (0 °F) e a temperatura normal do corpo humano (100 °F) como referências. 
Ele determinou que o ponto de fusão da água é de 32 °F e o ponto de ebulição é de 212 °F. 
Uso da escala
A escala foi utilizada principalmente nos países que foram colonizados pelos britânicos. 
Atualmente, o seu uso se restringe a poucos países de língua inglesa, como os Estados Unidos e Belize. 
A maioria dos países que ainda usavam a escala Fahrenheit optaram pela “metricação” em meados da década de 1990. 
Curiosidade 
Uma diferença de 1,8 °F é igual a uma diferença de 1 °C.'''
